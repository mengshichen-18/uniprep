TRAIN_LOG_DIR="${TRAIN_LOG_DIR}" \
ARTIFACT_ROOT="${ARTIFACT_ROOT}" \
ATOM_GEN_MODE="${ATOM_GEN_MODE}" \
SYMBOLIC_GEN_MODE="${SYMBOLIC_GEN_MODE}" \
