ATOM_MODEL="${ATOM_MODEL}" \
ATOM_BASE_URL="${ATOM_BASE_URL}" \
ATOM_MAX_REPAIR_ATTEMPTS="${ATOM_MAX_REPAIR_ATTEMPTS}" \
ATOM_ALLOW_EXTRA_FEATURES_TRUNCATE="${ATOM_ALLOW_EXTRA_FEATURES_TRUNCATE}" \
SYMBOLIC_MODEL="${SYMBOLIC_MODEL}" \
