EM_PAIR_CACHE_ROOT_BASE="${EM_PAIR_CACHE_ROOT_BASE:-${REPO_DIR}/outputs/em_pair_cache}"
EM_ROW_STATS_MODE="${EM_ROW_STATS_MODE:-full}"
FORCE_ATOM_REGEN="${FORCE_ATOM_REGEN:-0}"
FORCE_SYMBOLIC_REGEN="${FORCE_SYMBOLIC_REGEN:-0}"
SKIP_TRAIN="${SKIP_TRAIN:-0}"

if [[ ! -x "${MAIN_LAUNCHER}" ]]; then
  echo "[ERROR] main launcher missing: ${MAIN_LAUNCHER}" >&2
  exit 2
fi

echo "[INFO] launching no-symbolic ablation"
echo "[INFO] generated atoms may still be prepared if missing, but they will not be fed into the model"
echo "[INFO] session_prefix=${SESSION_PREFIX}"
echo "[INFO] seeds=${SEEDS_CSV} tasks=${TASKS_CSV} datasets=${DATASETS_CSV} gpus=${GPU_IDS_CSV}"

SEEDS_CSV="${SEEDS_CSV}" \
TASKS_CSV="${TASKS_CSV}" \
DATASETS_CSV="${DATASETS_CSV}" \
